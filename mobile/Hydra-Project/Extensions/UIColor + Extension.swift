import UIKit

extension UIColor {
    static func mainGrey() -> UIColor {
        UIColor(red: 0.9, green: 0.9, blue: 0.9, alpha: 1)
    }
    
    static func mainRed() -> UIColor {
        UIColor(red: 0.815, green: 0.07, blue: 0.105, alpha: 1)
    }
    
    static func mainWhite() -> UIColor {
        UIColor(red: 0.968, green: 0.972, blue: 0.992, alpha: 1)
    }

    static func mainDark() -> UIColor {
        UIColor(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }

}
